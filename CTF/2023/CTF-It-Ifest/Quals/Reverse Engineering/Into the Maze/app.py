class Game:
    def __init__(self, width, height, maze_layout):
        self.width = width
        self.height = height
        self.screen = pygame.display.set_mode((width, height))
        pygame.display.set_caption("Maze Game")
        self.maze = Maze(maze_layout, width, height)
        self.player = Player(self.maze.cell_size)

    def is_win(self):
        return self.player.x >= self.width - self.maze.cell_size and self.player.y >= self.height - (self.maze.cell_size * 2)

    def run(self):
        running = True
        while running:
            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    running = False
                elif event.type == pygame.KEYDOWN:
                    self.player.move(event.key, self.maze)

            if self.is_win():
                key = hashlib.md5(str(self.player.state).encode()).digest()
                iv = hashlib.md5(str(self.player.x).encode() + str(self.player.y).encode() + str(self.player.move_count).encode()).digest()

                try:
                    cipher = AES.new(key, AES.MODE_CBC, iv)
                    flag = unpad(cipher.decrypt(bytes.fromhex(FLAG)), AES.block_size).decode()

                    print(flag)
                except:
                    print("Invalid moves!")
                running = False
                break

            self.screen.fill(WHITE)
            self.maze.draw(self.screen)
            self.player.draw(self.screen)
            pygame.display.flip()

        pygame.quit()
        sys.exit()

class Maze:
    def __init__(self, layout, width, height):
        self.CELL_COLOR = {
            0: WHITE,
            1: BLACK,
            2: YELLOW
        }
        self.layout = layout
        self.cell_size = 10

    def draw(self, screen):
        for y, row in enumerate(self.layout):
            for x, cell in enumerate(row):
                rect = pygame.Rect(x * self.cell_size, y * self.cell_size, self.cell_size, self.cell_size)
                color = self.CELL_COLOR[cell]
                pygame.draw.rect(screen, color, rect)

        pygame.draw.rect(screen, RED, (0, self.cell_size, self.cell_size, self.cell_size))
        pygame.draw.rect(screen, GREEN, (len(self.layout[0]) * self.cell_size - self.cell_size, len(self.layout) * self.cell_size - (self.cell_size * 2), self.cell_size, self.cell_size))

    def is_valid_move(self, x, y):
        if x < 0 or y < 0 or x >= len(self.layout[0]) * self.cell_size or y >= len(self.layout) * self.cell_size:
            return False
        return self.layout[y // self.cell_size][x // self.cell_size] in [0, 2]

class Player:
    def __init__(self, size):
        self.x = 0
        self.y = size
        self.size = size
        self.speed = size
        self.state = [0] * 32
        self.state_pos = 0
        self.move_count = 0

    def move(self, key, maze):
        if key == pygame.K_LEFT:
            new_x = self.x - self.speed
            if maze.is_valid_move(new_x, self.y):
                maze.layout[self.y // self.size][self.x // self.size] = 2
                self.x = new_x

            self.state_pos -= 1
            if self.state_pos < 0:
                self.state_pos = 0

        elif key == pygame.K_RIGHT:
            new_x = self.x + self.speed
            if maze.is_valid_move(new_x, self.y):
                maze.layout[self.y // self.size][self.x // self.size] = 2
                self.x = new_x

            self.state_pos += 1
            if self.state_pos >= len(self.state):
                self.state_pos = len(self.state) - 1
        elif key == pygame.K_UP:
            new_y = self.y - self.speed
            if maze.is_valid_move(self.x, new_y):
                maze.layout[self.y // self.size][self.x // self.size] = 2
                self.y = new_y

            self.state[self.state_pos] = 1
        elif key == pygame.K_DOWN:
            new_y = self.y + self.speed
            if maze.is_valid_move(self.x, new_y):
                maze.layout[self.y // self.size][self.x // self.size] = 2
                self.y = new_y

            self.state[self.state_pos] = 0

        self.move_count += 1

    def draw(self, screen):
        pygame.draw.rect(screen, ORANGE, (self.x, self.y, self.size, self.size))

